nome = input("Digite seu nome")
print(nome)

while True: # Loop para repetir as operações enquanto o usuário quiser

    num1 = float(input("Digite o primeiro número: "))
    num2 = float(input("Digite o segundo número: "))

    # Escolha da operação
    print("Escolha a operação desejada:")
    print("1: Soma (+)")
    print("2: Subtração (-)")
    print("3: Multiplicação (*)")
    print("4: Divisão (/)")
    operacao = input("Digite o número da operação (1, 2, 3 ou 4): ")

    # Executando a operação escolhida
    if operacao == "1":
        resultado = num1 + num2
        print("Resultado da soma:", resultado)
    elif operacao == "2":
        resultado = num1 - num2
        print("Resultado da subtração:", resultado)
    elif operacao == "3":
        resultado = num1 * num2
        print("Resultado da multiplicação:", resultado)
    elif operacao == "4":
        if num2 != 0:
            resultado = num1 / num2
            print("Resultado da divisão:", resultado)
        else:
            print("Erro: divisão por zero não é permitida.")
    else:
        print("Operação inválida. Tente novamente.")

    # Pergunta se o usuário quer fazer outra operação
    repetir = input("Deseja fazer outra operação? (s para sim / qualquer outra tecla para sair): ")
    if repetir.lower() != "s":
        print("Obrigado por usar a calculadora. Até a próxima!")
        break